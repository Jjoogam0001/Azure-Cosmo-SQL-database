        // GET: Routes
        public async Task<ActionResult> Index()
        {
            await this.client.CreateDatabaseIfNotExistsAsync(new Microsoft.Azure.Documents.Database() { Id = "CharterResor" });
            await client.CreateDocumentCollectionIfNotExistsAsync(UriFactory.CreateDatabaseUri("CharterResor"),
                new DocumentCollection { Id = "Route" });

            FeedOptions queryOptions = new FeedOptions { EnableCrossPartitionQuery = true };
            IQueryable<Route> routes = this.client.CreateDocumentQuery<Route>(
                    UriFactory.CreateDocumentCollectionUri("CharterResor", "Route"), queryOptions)
                ;


            return View(routes);




            return View(db.Routes.ToList());
        }

        // GET: Routes/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Route route = db.Routes.Find(id);
            if (route == null)
            {
                return HttpNotFound();
            }
            return View(route);
        }

        // GET: Routes/Create
        public ActionResult Create()
        {
